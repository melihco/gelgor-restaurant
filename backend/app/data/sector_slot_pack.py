from __future__ import annotations

from typing import TypedDict


class SlotInstance(TypedDict, total=False):
    suffix: str
    label_tr: str
    label_en: str
    format: str
    optional_tags: list[str]
    enabled_by_default: bool


class SectorPack(TypedDict):
    sector_id: str
    label_tr: str
    label_en: str
    aliases: list[str]
    sort_order: int
    instances: list[SlotInstance]


def _inst(
    suffix: str,
    label_tr: str,
    label_en: str,
    fmt: str,
    optional_tags: list[str] | None = None,
) -> SlotInstance:
    row: SlotInstance = {
        "suffix": suffix,
        "label_tr": label_tr,
        "label_en": label_en,
        "format": fmt,
    }
    if optional_tags:
        row["optional_tags"] = optional_tags
    return row


